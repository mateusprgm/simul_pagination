<script>
	let start = 0;
	let finish = 4;
	let limit = 4;
	let page = 1;
	let perpage = true;

	let finish_pvnx = finish;

	
	
	function goPage(pageDoc){
		if(pageDoc != page){
			if(pageDoc > page){
				nextPage(pageDoc, false);
			}else{
				prevPage(pageDoc, false);
			}
		}
	}



	function nextPage(X, onePage){
		let nPage = document.getElementById(`nPage`).value;
		let aux = nPage - page;
		if(aux > 0){
			console.log("1start: "+start+" - finish: "+finish);
		
				start += limit;
				finish += limit;
			
			console.log("1start: "+start+" - finish: "+finish);
			

			let start_jump = start;
			let finish_jump = finish;

			
			
			let prevNext = true;
			
		
		
			console.log(aux);
			if(!onePage){
				if(X > 1){
					start_jump = ((X*finish_pvnx)-finish_pvnx);
					start = start_jump;
					finish_jump = (X*(finish_pvnx));
					finish = finish_jump;
				}
				if(X >= 1){
					page = 0;
					for (let i = 0; i < X; i++) {
						document.getElementById(`page`).value = page++;
					}
				}
			}else{

				document.getElementById(`page`).value = page++;
				console.log("page: "+page);
			}

			pagination(6, 'interestList', start_jump, finish_jump, prevNext);
		}
		
	}

	function prevPage(X, onePage){
		if(page > 1){

			start -=  limit;
			finish -= limit;
		
			let	start_jump = start;
			let finish_jump = finish;

			let prevNext = true;

		
			
			if(!onePage){
				if(page > X ){
					start_jump = ((X*finish_pvnx)-finish_pvnx);
					start = start_jump;
					finish_jump = (X*(finish_pvnx));
					finish = finish_jump;
				}
				let pg = page-X;

				if(X >= 1){
					for (let i = 0; i < pg; i++) {
						document.getElementById(`page`).value = page--;
					}
				}
			}else{
				document.getElementById(`page`).value = page--;
				console.log("page: "+page);
			}
		
			pagination(6, 'interestList', start_jump, finish_jump, prevNext);
		}
	}


	let lock = true;


	function pagination(limit, id, start, finish, prevNext){
		
		document.getElementById(`page`).value = page;

		let nItems = document.getElementById(`${id}`).childElementCount;
		let nPage = nItems/limit;

		
		
		if(!Number.isInteger(nPage)){
			nPage = Math.ceil(nPage);
		}
		if(lock){
			document.getElementById(`nPage`).value = nPage;
			lock = false;
		}
		
		if(perpage){
			let li;
			let active = '';
			for (let i = 0; i < nPage; i++) {
				if(i == 0){
					active = 'active';
				}
				li = liFactory(i, active);
				document.getElementById("paginationUL").innerHTML += li;
				active = '';
			}
			perpage = false;
		}
		
		if(prevNext){
			for (let i = 0; i < nItems; i++) {
				if(document.getElementById(`${id}`).children[i]!=undefined){
					try{	
						document.getElementById(`paginationUL`).children[i].classList.remove("active");
					}catch(err){
						//console.log(err);
					}
					document.getElementById(`${id}`).children[i].style.display = 'none';
					
				}
			}
			
		}
		document.getElementById(`per${page}`).classList.add("active");
		// console.log("start: "+start+" - finish: "+finish);
		for (let i = start; i < finish; i++) {
			if(document.getElementById(`${id}`).children[i]!=undefined){
				document.getElementById(`${id}`).children[i].style.display = 'block';
			}
		}
	}


	function liFactory(page, active){
		return `<li class="page-item ${active}" id="per${page+1}" onclick="goPage(${page+1})"><a class="page-link" href="#interestList">${page+1}</a></li>`;
	}


	pagination(limit, 'interestList', start, finish, false);
</script>